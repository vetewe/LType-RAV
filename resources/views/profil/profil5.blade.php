            <h2>KARAKTERISTIK AVK - SANG KOMUNIKATOR</h2>
            <p>Tipe belajar AVK (Auditori, Visual, dan Kinestetik) adalah suatu pendekatan dalam proses belajar mengajar yang memperhatikan tiga jenis modalitas atau cara belajar siswa, yaitu belajar dengan mendengar (auditori), belajar dengan mengingat (visual) dan belajar dengan gerak dan emosi (kinestetik).</p>
            <p>Pertama, gaya belajar auditori merujuk pada individu yang lebih efektif dalam memahami informasi melalui pendengaran. Mereka cenderung lebih mudah memahami konsep atau materi jika disampaikan melalui ceramah, diskusi, atau mendengarkan penjelasan.</p>
            <p>Kedua, gaya belajar visual merujuk pada individu yang lebih efektif dalam memahami informasi melalui penglihatan. Mereka cenderung lebih mudah memahami konsep atau materi jika disajikan dalam bentuk gambar, diagram, atau visualisasi lainnya.</p>
            <p>Ketiga, gaya belajar kinestetik merujuk pada individu yang lebih efektif dalam memahami informasi melalui gerakan dan emosi. Mereka cenderung lebih mudah memahami konsep atau materi jika melibatkan aktivitas fisik atau emosi.</p>
