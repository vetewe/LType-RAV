            <p>Model pembelajaran VAK menekankan bahwa belajar haruslah memanfaatkan alat indra yang dimiliki. Pembelajaran dengan model pembelajaran Visual Auditori Kinestetik (VAK) adalah suatu pembelajaran yang memanfaatkan gaya belajar setiap individu dengan tujuan agar semua kebiasaan belajar akan terpenuhi. Model pembelajaran ini memberikan kesempatan kepada kita untuk belajar langsung dengan bebas menggunakan modalitas yang dimilikinya untuk mencapai pemahaman dan pembelajaran yang efektif.</p> -->
